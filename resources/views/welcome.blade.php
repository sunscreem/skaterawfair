            <div class="col-md-4 question">
              <p>Is there an admission price?</p>
            </div>
            <div class="col-md-8 answer">
                <p>The Fair Day itself is a free event, however donations for the lucky programme are encouraged. The Skateraw Fair has many operating costs and any support through volunteering or donations is gratefully received.</p>
                <p>The football sessions are also free events. There will be a BBQ and/or tuck shop which helps support the running costs.</p>
                <p>The Fair Dance is a paid event and tickets are available now.</p>
            </div>
        </div>

        <div class="row">
            <div class="col-md-4 question">
              <p>What is the best way to get to the Fair Day?</p>
            </div>
            <div class="col-md-8 answer">
                <p>If you are within walking distance please leave the car at home, the limited parking at the Bettridge Centre is reserved for disabled badge holders.</p>
                <p>We always welcome new faces at the Fair and if you are travelling from outside Newtonhill  there are <a href="https://tiscon-maps-stagecoachbus.s3.amazonaws.com/Timetables/North%20Scotland/Bluebird/TT_Stonehaven_Guide_08May17-web.pdf">frequent bus services</a> from Aberdeen and Stonehaven.</p>
            </div>
        </div>

        <div class="row">
            <div class="col-md-4 question">
                <p>Can I bring my dog?</p>
            </div>
            <div class="col-md-8 answer">
                <p>Of course, there are plenty of them in Newtonhill! Please keep your pooch on a lead at all times.</p>
                <p>Unfortunately, with the exception of guide dogs, pets are not allowed into the Bettridge Centre.</p>
            </div>
        </div>

        <div class="row">
            <div class="col-md-4 question">
                <p>Can I bring my own alcohol?</p>
